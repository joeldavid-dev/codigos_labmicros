processor 16f877
include <p16f877.inc>

R1 EQU 0x20

	ORG 0 
	GOTO INICIO
	ORG 5 			;INICIO PROGRAMA
 	

INICIO:

	CLRW 			; LIMPIAR W
	CLRF R1			;LIMIPIAR R1
	CLRF STATUS		;LIMPIAR BANDERAS
	GOTO REINICIO

REINICIO:
	MOVLW 0x01		;W=1
	MOVWF R1 		;W=R1
	GOTO ROTAR
	
ROTAR:
	RLF R1 ; CORRIMIENTO DE BITS
	BTFSS R1,7 ; SI BIT 7 = 1 ES 80 HEXA
	GOTO ROTAR
	GOTO REINICIO

	END